    private void jButtonsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsaveActionPerformed
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection
                     ("jdbc:mysql://localhost:3306/admin_login","root","");

            PreparedStatement pstmt1 = con.prepareStatement("INSERT INTO midresult (studentid,tbw, construction, networking, financial, formal, mpl) VALUES ((SELECT studentid FROM student WHERE studentid = ?),?,?,?,?,?,?)");

                pstmt1.setInt(1, Integer.parseInt( jTextFieldstudentid.getText()));
                pstmt1.setString(2, jTextFieldtbw.getText());
                 pstmt1.setString(3, jTextField1software.getText());
                 pstmt1.setString(4, jTextFieldnetworking.getText());
                 pstmt1.setString(5, jTextFieldfinancial.getText());
                  pstmt1.setString(6, jTextFieldformal.getText());
                pstmt1.setString(7, jTextFieldmpl.getText());
                int rs = pstmt1.executeUpdate();
              
                if (rs>0)
                {
                JOptionPane.showMessageDialog(null, "Successfully updated!!");
                 jTextFieldstudentid.setText("");
                jTextFieldfinancial.setText("");
                jTextField1software.setText("");
                jTextFieldtbw.setText("");
                jTextFieldformal.setText("");
                jTextFieldnetworking.setText("");
                jTextFieldmpl.setText("");
                
                }
            else{
                JOptionPane.showMessageDialog(null, "Roll Number does not exits in our database!");
            }

        }  catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButtonsaveActionPerformed

