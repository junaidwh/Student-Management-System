    private void jButtonshowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonshowActionPerformed
        // TODO add your handling code here:

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/admin_login","root","");
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM finalresult");
            DefaultTableModel model = new DefaultTableModel();

            model.addColumn("studentid");
            model.addColumn("tbw");
            model.addColumn("construction");
            model.addColumn("networking");
            model.addColumn("financial");
            model.addColumn("formal");
            model.addColumn("mpl");

            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("studentid"),rs.getString("tbw"),
                    rs.getString("construction"),rs.getString("networking"),
                    rs.getString("financial"),rs.getString("formal"),
                    rs.getString("mpl")

                });

            }

            jTable1.setModel(model);

            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButtonshowActionPerformed

